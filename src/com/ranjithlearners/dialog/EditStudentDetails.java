    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        String dsr = txtID.getText().trim();
        
        try {
            MySQL db = new MySQLProxy();

            // Update student table
            String updateStudentSql = "UPDATE student SET name = ?, dob = ?, mobile = ?, nic = ?, medical_no = ?, address = ? WHERE id = ?";
            db.executeIUD(updateStudentSql,
                    AESUtil.encrypt(txtName.getText().trim()),
                    txtDOB.getText().trim(),
                    AESUtil.encrypt(txtMobile.getText().trim()),
                    AESUtil.encrypt(txtNIC.getText().trim()),
                    AESUtil.encrypt(txtMedNo.getText().trim()),
                    AESUtil.encrypt(txtAddress.getText().trim()),
                    dsr
            );

            // Update course
            String course = comboCourse.getSelectedItem().toString();
            String updateCourseSql = "UPDATE student_has_course SET course_code = ? WHERE student_id = ?";
            db.executeIUD(updateCourseSql, course, dsr);
            
            db.close();
            
            Notifications.getInstance().show(Notifications.Type.SUCCESS,
                    Notifications.Location.TOP_CENTER,
                    3000,
                    "‚úÖ Student updated successfully.");
            clearFields();
            searchStudent();
        } catch (Exception e) {
            e.printStackTrace();
            Notifications.getInstance().show(Notifications.Type.ERROR,
                    Notifications.Location.TOP_CENTER,
                    3000,
                    "‚ùå Failed to update student.");
        }
    }//GEN-LAST:event_editBtnActionPerformed

