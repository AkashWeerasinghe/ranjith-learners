    private void loadCourses() {
        try {
            MySQL db = new MySQLProxy();
            ResultSet rs = db.executeSearch("SELECT code FROM course");
            comboCourse.removeAllItems();
            while (rs.next()) {
                comboCourse.addItem(rs.getString("code"));
            }
            db.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private void searchStudent() {
        String dsr = txtID.getText().trim();
        String name = txtName.getText().trim();
        
        if (dsr.isEmpty() && name.isEmpty()) {
            return;
        }
        
        try {
            MySQL db = new MySQLProxy();
            String sql = "SELECT s.id, s.name, s.dob, s.mobile, s.nic, s.medical_no, s.address, sc.course_code "
                    + "FROM student s "
                    + "LEFT JOIN student_has_course sc ON s.id = sc.student_id "
                    + "WHERE s.id = ? OR s.name = ?";
            ResultSet rs = db.executeSearch(sql, dsr, AESUtil.encrypt(name));
            
            if (rs.next()) {
                txtID.setText(rs.getString("id"));
                txtName.setText(AESUtil.decrypt(rs.getString("name")));
                txtDOB.setText(rs.getString("dob"));
                txtNIC.setText(AESUtil.decrypt(rs.getString("nic")));
                txtMobile.setText(AESUtil.decrypt(rs.getString("mobile")));
                txtMedNo.setText(rs.getString("medical_no") != null ? AESUtil.decrypt(rs.getString("medical_no")) : "");
                txtAddress.setText(AESUtil.decrypt(rs.getString("address")));
                
                String course = rs.getString("course_code");
                if (course != null) {
                    comboCourse.setSelectedItem(course);
                }
                
                Notifications.getInstance().show(Notifications.Type.SUCCESS,
                        Notifications.Location.TOP_CENTER,
                        3000,
                        "üü¢ Student loaded");
            } else {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER,
                        3000,
                        "üü° No matching student found");
                clearFields();
            }
            
            rs.close();
            db.close();
        } catch (Exception e) {
            e.printStackTrace();
            Notifications.getInstance().show(Notifications.Type.ERROR,
                    Notifications.Location.TOP_CENTER,
                    3000,
                    "üî¥ Error");
        }
    }
    
